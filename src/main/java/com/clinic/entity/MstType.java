package com.clinic.entity;

public class MstType {
	
	private int id;
	private String mst_tbl;
	private String mst_type_name;
	private String status;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMst_tbl() {
		return mst_tbl;
	}
	public void setMst_tbl(String mst_tbl) {
		this.mst_tbl = mst_tbl;
	}
	public String getMst_type_name() {
		return mst_type_name;
	}
	public void setMst_type_name(String mst_type_name) {
		this.mst_type_name = mst_type_name;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}


}
