package com.clinic.api.object;

public class Graph {
	
	private String type;
	private int month;
	private String categogry;
	private double value;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public String getCategogry() {
		return categogry;
	}
	public void setCategogry(String categogry) {
		this.categogry = categogry;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	
	

}
